package Strings;

public class PrettyPrinting {

    public static void main(String[] args) {
        
        float a = 456.1234f;


        // Pretty printing using placeholder
        System.out.printf("Formatted number is %.2f", a);
        System.out.println();

        System.out.printf("Pie: %.3f\n", Math.PI);


        System.out.printf("Hello my name is %s and I am %s", "Abdul Moid", "Student");
    }

}
